package Tasks;

public class Fitness {
            public static int fitCalc(int level, int time ) {
                return time * level;
            
            }
        public static void main(String[] args) {
            int level = 2;
            int time = 15;
            int calories = fitCalc(level, time);
            System.out.println(calories);
        }
        
        }// ЗАдание 2 Фитнесс
    
    
    
        
        
    

